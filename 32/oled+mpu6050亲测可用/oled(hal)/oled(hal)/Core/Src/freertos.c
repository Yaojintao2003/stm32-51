/* USER CODE BEGIN Application */
void oled_Task(void const * argument){
	uint8_t str[50];
	float i=0,j=0;
	float fps=0;
	extern int num;
	//uint32_t time;
	vTaskDelay(100);
	OLED_Init();
		for(;;){
			//time = xTaskGetTickCount();
			if((xTaskGetTickCount()/1000)>j){
				i = xTaskGetTickCount()/1000 - j;
				j = xTaskGetTickCount()/1000;
				fps = (float)num/i;
				num = 0;
			}
			sprintf((char*)str,"%3d fps: %5.2f",num,fps);
			OLED_CLS();
			LCD_P8x16Str(0,4,(uint8_t*)str);
			memset(str,0,sizeof(str));
			show_oled();
			vTaskDelay(1);
		}
}
